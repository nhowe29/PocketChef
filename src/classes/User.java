package classes;

public class User 
{
	private int userID;
	private String username;
	private String password;
	private String firstName;
	private String lastName;
	private int access;
	
	public User(int userID, String username, String password, String firstName, String lastName, int access)
	{
		this.userID = userID;
		this.username = username;
		this.password = password;
		this.firstName = firstName;
		this.lastName = lastName;
		this.access = access;
	}
}
